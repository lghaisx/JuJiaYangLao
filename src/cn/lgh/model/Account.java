package cn.lgh.model;

public class Account {
    private int aid;
    private String alogin;
    private String apwd;
    private String areal;
    private int arank;
    private String aphone;
    private String aemail;

    public int getAid() {
        return aid;
    }

    public void setAid(int aid) {
        this.aid = aid;
    }

    public String getAlogin() {
        return alogin;
    }

    public void setAlogin(String alogin) {
        this.alogin = alogin;
    }

    public String getApwd() {
        return apwd;
    }

    public void setApwd(String apwd) {
        this.apwd = apwd;
    }

    public String getAreal() {
        return areal;
    }

    public void setAreal(String areal) {
        this.areal = areal;
    }

    public int getArank() {
        return arank;
    }

    public void setArank(int arank) {
        this.arank = arank;
    }

    public String getAphone() {
        return aphone;
    }

    public void setAphone(String aphone) {
        this.aphone = aphone;
    }

    public String getAemail() {
        return aemail;
    }

    public void setAemail(String aemail) {
        this.aemail = aemail;
    }
}

